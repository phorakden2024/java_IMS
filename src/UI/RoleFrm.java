    private void btnSetRoleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSetRoleActionPerformed
        // Set Role
        try {
            DefaultTableModel tbl = (DefaultTableModel) this.jTable1.getModel();
            if (tbl.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "No record selected!");
                return;
            }
            int idx = this.jTable1.getSelectedRow();
            long user_id = dao.loadUserRole().get(idx).getUid();
            String user_name = dao.loadUserRole().get(idx).getUname();
            int role_id = dao.loadUserRole().get(idx).getRole_id();
            String role_name = dao.loadUserRole().get(idx).getRole_name();
            if (btnSetRole.getText().equals("Set Role")) {
                DialogSetRole setroleform = new DialogSetRole(null, user_id, user_name, role_id, role_name);
                if (idx == jTable1.getSelectedRow()) {
//                    System.out.println(user_id);
//                    System.out.println(user_name);
//                    System.out.println(role_id);
//                    System.out.println(role_name);
                    setroleform.setVisible(true);

                }

            }
        } catch (Exception e) {
            Logger.getLogger(RoleFrm.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnSetRoleActionPerformed

