    public void showUsers(List<UserDto> users) {
        //insert into jtable
        DefaultTableModel tbl = (DefaultTableModel) this.jTable1.getModel();
        tbl.setRowCount(0);//clear
        for (UserDto dto : users) {
            tbl.addRow(new Object[]{dto.getUid(), dto.getUname(), dto.getUpass(), dto.getRole_name()});
        }
        if (tbl.getRowCount() > 0) {
            this.jTable1.setRowSelectionInterval(0, 0);
        }
    }

